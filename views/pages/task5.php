<p><span class="first">Step 2:</span>  Think about what you want to include in the letter.  It is a good idea to include things you talked about during the interview.</p>

<p><span class="first">Step 3:</span>  Write a <a href="javascript:void(0);" data-toggle="popover" class="bold" title="Rough draft" data-content="Something that has been written but not yet edited for mistakes.">rough draft</a> of your follow-up letter or use a template.  </p>

<p><span class="first">Step 4:</span> Edit the letter yourself.   Fix any mistakes you find.  Then have one of your partners edit it for you.  If you are using a computer be sure to use the spell check.</p>

<p><span class="first">Step 5:</span> Finally, write your final draft or print it to turn in.</p>

